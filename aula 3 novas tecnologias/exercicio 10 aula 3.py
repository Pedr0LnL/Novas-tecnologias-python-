# Criando o dicionário com as informações da pessoa
informacoes_pessoa = {
    'first_name': 'João',
    'last_name': 'Silva',
    'age': 30,
    'city': 'São Paulo'
}

# Mostrando cada informação armazenada no dicionário
print("Primeiro nome:", informacoes_pessoa['first_name'])
print("Sobrenome:", informacoes_pessoa['last_name'])
print("Idade:", informacoes_pessoa['age'])
print("Cidade:", informacoes_pessoa['city'])
