temperaturas = [-10, -8, 0, 1, 2, 5, -2, -4]

# Encontrar a menor temperatura
menor_temperatura = min(temperaturas)

# Encontrar a maior temperatura
maior_temperatura = max(temperaturas)

# Calcular a temperatura média
temperatura_media = sum(temperaturas) / len(temperaturas)

# Imprimir os resultados
print("Menor temperatura:", menor_temperatura)
print("Maior temperatura:", maior_temperatura)
print("Temperatura média:", temperatura_media)
